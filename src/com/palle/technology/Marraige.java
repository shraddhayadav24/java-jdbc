package com.palle.technology;

public class Marraige {
	public static void main(String args[])
	{
		char ch1='F';
		char ch2='M';
		int age=23;
		if(ch1=='F' && age>=18)
		{
			System.out.println("Eligile");
		}
		else if(ch2=='M' && age>=21)
		{
			System.out.println("Eligible");
		}
		else
		{
			System.out.println("Not eligile");
		}
	}
}
